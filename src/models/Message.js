import mongoose from "mongoose";

const messageSchema = new mongoose.Schema(
  {
    /*
    |--------------------------------------------------------------------------
    | Chat Room
    |--------------------------------------------------------------------------
    */

    room: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "ChatRoom",
      required: true,
    },

    /*
    |--------------------------------------------------------------------------
    | Sender
    |--------------------------------------------------------------------------
    */

    sender: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },

    /*
    |--------------------------------------------------------------------------
    | Message Content
    |--------------------------------------------------------------------------
    */

    text: {
      type: String,
      required: true,
      trim: true,
      maxlength: 1000,
    },

    /*
    |--------------------------------------------------------------------------
    | Read Status
    |--------------------------------------------------------------------------
    */

    isRead: {
      type: Boolean,
      default: false,
    },

    /*
    |--------------------------------------------------------------------------
    | Read Timestamp
    |--------------------------------------------------------------------------
    */

    readAt: {
      type: Date,
      default: null,
    },
  },
  {
    timestamps: true,
  }
);

/*
|--------------------------------------------------------------------------
| Indexes
|--------------------------------------------------------------------------
*/

/*
|--------------------------------------------------------------------------
| Fetch Messages Of A Room
|--------------------------------------------------------------------------
*/

messageSchema.index({
  room: 1,
  createdAt: 1,
});

/*
|--------------------------------------------------------------------------
| Unread Messages
|--------------------------------------------------------------------------
*/

messageSchema.index({
  room: 1,
  isRead: 1,
});

const Message = mongoose.model(
  "Message",
  messageSchema
);

export default Message;